<?php

namespace App\DTO;

/**
 * Class AuthorDTO
 * @package App\DTO
 */
class AuthorDTO
{
    /**
     * @var string
     */
    private $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}
